class Variables
{
    public static void main(String[] args)
    {
        double a = 23.23452121;
        float b = 3.12F;
        int c = 12 , d = 6;
        byte e = 100;
        System.out.print(a + " " + b + " " + c + " " + d + " " + e);
        //Cada vez que se declare float se debe poner una F para que java lo reconozca de lo contrario creera que es un double
    }
}